					<h4 class="tt">11. Criteria for Pass:</h4>
					<p> 1. No candidate shall be eligible for the award of Diploma unless he/she has undergone the prescribed course of study successfully in an institution approved by AICTE and affiliated to the State Board of Technical Education &amp; Training, Tamil Nadu and pass all the subjects prescribed in the curriculum. <br>
					 2. A candidate shall be declared to have passed the examination in a subject if he/she secures not less than 40% in theory subjects and 50% in practical subject out of the total prescribed maximum marks including both the internal assessment and the Board Examination marks put together, subject to the condition that he/she secures at least a minimum of 30 marks out of 75 marks in the Board’s Theory examinations and a minimum of 35 marks out of 75 marks in the Board Practical Examinations. <br> </p>


					<h4 class="tt">12. Classification of successful candidates:</h4>
					<p> Classification of candidates who will pass out the final examinations from April 2018 onwards (Joined in first year in 2015-2016) will be done as specified below.
<br>First Class with Superlative Distinction: <br> <br>
A candidate will be declared to have passed in First Class with Superlative Distinction if he/she secures not less than 75% of the marks in all the subjects and passes all the semesters in the first appearance itself and passes all subjects within the stipulated period of study 3/ 3½/ 4 years (Full  Time/Sandwich/Part Time) without any break in study.

					<br><br>First Class with Distinction: <br> <br>
					A candidate will be declared to have passed in First Class with Distinction if he/she secures not less than 75% of the aggregate of marks in all the semesters put together and passes all the semesters except the I and II semesters in the first appearance itself and passes all the subjects within the stipulated period of study 3/ 3½/ 4 years (Full Time/Sandwich/Part Time) without any break in study.
					<br> <br>First Class: <br> <br>
					A candidate will be declared to have passed in First Class if he/she secures not less than 60% of the aggregate marks in all semesters put together and passes all the subjects within the stipulated period of study 3/ 3½ / 4 years (Full Time/Sandwich/Part Time) without any break in study.
					<br> <br>Second Class: <br> <br>
					All other successful candidates will be declared to have passed in Second Class. The above mentioned classifications are also applicable for the Sandwich / Part- Time students who pass out Final Examination from October 2018 /April 2019 onwards (both joined in First Year in 2015-2016) </p>

					<h4 class="tt">13. Duration of a period in the Class Time Table:</h4>
					<p> The duration of each period of instruction is 1 hour and the total period of instruction hours excluding interval and Lunch break in a day should be uniformly maintained as 7 hours corresponding to 7 periods of instruction (Theory &amp; Practical). </p>

					<h4 class="tt">14. Seminar:</h4>
					<p> For seminar the total seminar 15 hours(15 weeks x 1hour) should be distributed equally to total theory subject per semester(i.e 15 hours divided by 3/4 subject). A topic from subject or current scenario is given to students. During the seminar hour students have to present the paper and submit seminar material to the respective staff member, who is handling the subject. It should be preserved for 2 Semesters and produced to the flying squad and the inspection team at the time of inspection/verification. </p>


