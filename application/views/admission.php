<h3 class="tt">M – SCHEME REGULATIONS*</h3>
<h4 class="tt">*Applicable to the Diploma Courses other than Diploma in Hotel Management &amp; Catering Technology and the Diploma Courses offered through MGR Film Institute, Chennai.</h4>
<h4 class="tt">1.	Description of the Course :</h4>
				<h4 class="tt">a.	Full Time (3 years) </h4>
                <p>
                   The Course for the Full Time Diploma in Engineering shall extend over a period of three academic years, consisting of 6 semesters* and the First Year is common to all Engineering Branches.
                </p>
				<h4 class="tt">b.	Sandwich (3½ years) </h4>
				<p>The Course for the Diploma in Engineering (sandwich) shall extend over a period of three and half academic years, consisting of 7 semesters* and the First Year is common to all Engineering Branches. The subjects of three years full time diploma course being regrouped for academic convenience. </p>
				<p>During 4th and/or during 7th semester the students undergo industrial training for six months/ one year. Industrial training examination will be conducted after completion of every 6 months of industrial training </p>
              <h4 class="tt"> c.	Part Time (4 years)</h4>

<p>The course for the Diploma in Engineering shall extend over a period of 4 academic years containing of 8 semesters*, the subjects of 3 year full time diploma courses being regrouped for academic convenience. </p>

<p>*Each Semester will have 15 weeks duration of study with 35 hrs./Week for Regular Diploma Programme and 18hrs/ week (21 hrs./ Week I year) for Part- Time Diploma Programmes.</p>

<p>The Curriculum for all the 6 Semesters of Diploma courses (Engineering &amp; Special Diploma Courses viz. Textile Technology, Leather Technology, Printing Technology, Chemical Technology etc.) have been revised and revised curriculum is applicable for the candidates admitted from 2015 – 2016 academic year onwards.</p>

